package cn.meteor.beyondclouds.common.exception;
/**
 * QQ 认证异常
 *
 * @author 段启岩
 */
public class QQAuthenticationException extends java.lang.Exception {
    public QQAuthenticationException() {
    }

    public QQAuthenticationException(java.lang.String message) {
        super(message);
    }

    public QQAuthenticationException(java.lang.String message, java.lang.Throwable cause) {
        super(message, cause);
    }
}